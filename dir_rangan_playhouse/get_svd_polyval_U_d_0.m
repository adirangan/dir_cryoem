function svd_polyval_U_d_ = get_svd_polyval_U_d_0(svd_d_max,n_svd_d,svd_d_,n_svd_l,svd_l_,svd_U_d_,n_delta_v,delta_x_,delta_y_);
verbose=0; flag_warning=1;
if (verbose>0); disp(sprintf(' %% [entering get_svd_polyval_U_d_]')); end;
svd_d_m = svd_d_max / 2.0d0;
svd_d_c = svd_d_m;
for ndv=0:n_delta_v-1;
delta_x = delta_x_(1+ndv);
delta_y = delta_y_(1+ndv);
delta_r = sqrt(delta_x^2 + delta_y^2);
if (delta_r>svd_d_max & flag_warning);
disp(sprintf(' %% Warning, delta_r %0.2f > svd_d_max %0.2f',delta_r,svd_d_max));
end;%if (delta_r>svd_d_max & flag_warning);
delta_w = atan2(delta_y,delta_x);
svd_d = (delta_r - svd_d_m)/svd_d_c; if (~isfinite(svd_d)); svd_d=0; end;
for nl=0:n_svd_l-1;
svd_polyval_U_d_(1+nl+ndv*n_svd_l) = polyval_r8_reverse_0(n_svd_d,svd_U_d_(1+0+nl*n_svd_d+(0:n_svd_d-1)),1,svd_d);
end;%for nl=0:n_svd_l-1;
end;%for ndv=0:n_delta_v-1;
if (verbose>0); disp(sprintf(' %% [finished get_svd_polyval_U_d_]')); end;
