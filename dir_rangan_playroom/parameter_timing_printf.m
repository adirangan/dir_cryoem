function parameter = parameter_timing_printf(parameter);

na=0;
if nargin<1+na; parameter=[]; end;

if isempty(parameter); parameter = struct('type','parameter'); end;%if isempty(parameter);
%%%%%%%%;
if (~isfield(parameter,'timing'));
parameter.timing = cell(1,6);
parameter.timing{1,1} = 'label';
parameter.timing{1,2} = 'total time';
parameter.timing{1,3} = 'number of calls';
parameter.timing{1,4} = 'number of ops';
parameter.timing{1,5} = 'MHz';
parameter.timing{1,6} = 'GHz';
end; %<-- parameter_bookmark. ;
%%%%%%%%;
n_label = size(parameter.timing,1)-1;
if (n_label> 0);
label_ = parameter.timing(2:n_label+1,1);
t_tot_ = cell2mat(parameter.timing(2:n_label+1,2));
n_cal_ = cell2mat(parameter.timing(2:n_label+1,3));
n_ops_ = cell2mat(parameter.timing(2:n_label+1,4));
MHz_   = cell2mat(parameter.timing(2:n_label+1,5));
GHz_   = cell2mat(parameter.timing(2:n_label+1,6));
[t_srt_,ij_srt_] = sort(t_tot_,'descend'); index_srt_ = ij_srt_-1;
disp(sprintf(' %% %16.16ss %16.16s %16.16s %s','t_tot','n_cal','n_ops','label'));
for nlabel=0:n_label-1;
index = index_srt_(1+nlabel);
label = label_(1+index);
t_tot = t_tot_(1+index);
n_cal = n_cal_(1+index);
n_ops = n_ops_(1+index);
if ~isempty(MHz_); MHz = MHz_(1+index); end;
if ~isempty(GHz_); GHz = GHz_(1+index); end;
disp(sprintf(' %% %+16.6fs %+16.4d %+16.4d %s',t_tot,n_cal,n_ops,label{1}));
end;%for nlabel=0:n_label-1;
end;%if (n_label> 0);
%%%%%%%%;
