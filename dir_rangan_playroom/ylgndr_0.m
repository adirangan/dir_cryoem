function y_jlm___ = ylgndr_0(l_max,x_);
% Note that this returns: ;
% y_jlm___(1+nx,1+l_val,1+m_val) ; 
% such that ;
% y__(1+nx,1+l_val,1+m_val) == Y__(1+l_val,1+m_val)(x_(1+nx)), where: ;
% Y__ is constructed via: ;
% m_val_=transpose(0:l_val);
% tmp_a1 = ((1+2*l_val)/(4*pi));
% tmp_a2_ = factorial(l_val-abs(m_val_))./factorial(l_val+abs(m_val_));
% tmp_a3_ = sqrt(tmp_a1*tmp_a2_);
% Y__(1+l_val,:) = legendre(l_val,x,'unnorm').*tmp_a3_*sqrt(4*pi);

if (nargin<2);
l_max = 4; n_x = 25;
x_ = transpose(2*rand(n_x,1)-1);
tmp_y_fortran___ = ylgndr_0(l_max,x_);
tmp_Y_matlab___ = zeros(n_x,1+l_max,1+l_max);
for l_val=0:l_max;
m_val_ = transpose(0:l_val);
tmp_a1 = ((1+2*l_val)/(4*pi));
tmp_a2_ = exp(lfactorial(l_val-abs(m_val_)) - lfactorial(l_val+abs(m_val_)));
tmp_a3_ = sqrt(tmp_a1*tmp_a2_);
for nx=0:n_x-1;
tmp_Y_matlab___(1+nx,1+l_val,1:(1+l_val)) = legendre(l_val,x_(1+nx),'unnorm').*tmp_a3_;
end;%for nx=0:n_x-1;
end;%for l_val=0:l_max;
disp(sprintf(' %% tmp_y_fortran___ vs sqrt(4*pi)*tmp_Y_matlab___: %0.16f',fnorm(tmp_y_fortran___-sqrt(4*pi)*tmp_Y_matlab___)/fnorm(tmp_y_fortran___)));
l_max = 96; n_x = 1024*2;
x_ = rand(n_x,1);
tmp_t = tic;
ylgndr_0(l_max,x_);
tmp_t = toc(tmp_t); disp(sprintf(' %% y_fortran___: %0.2fs',tmp_t));
tmp_t = tic;
for l_val=0:l_max;
m_val_ = transpose(0:l_val);
tmp_a1 = ((1+2*l_val)/(4*pi));
tmp_a2_ = exp(lfactorial(l_val-abs(m_val_)) - lfactorial(l_val+abs(m_val_)));
tmp_a3_ = sqrt(tmp_a1*tmp_a2_);
legendre(l_val,x_,'unnorm').*tmp_a3_;
end;%for l_val=0:l_max;
tmp_t = toc(tmp_t); disp(sprintf(' %% Y_matlab___: %0.2fs',tmp_t));
disp('returning'); return;
end;%if (nargin<2);

x_ = x_(:);
n_x = numel(x_);
u_ = -sqrt((1-x_).*(1+x_));
y_jlm___ = zeros(n_x,l_max+1,l_max+1);
y_jlm___(:,1+0,1+0) = 1;
for m_val=0:l_max;
if (m_val>0); y_jlm___(:,1+m_val,1+m_val) = y_jlm___(:,1+m_val-1,1+m_val-1).*u_*sqrt((2*m_val-1)/(2*m_val)); end;
if (m_val<l_max); y_jlm___(:,1+m_val+1,1+m_val) = x_.*y_jlm___(:,1+m_val,1+m_val)*sqrt(2*m_val+1); end;
for l_val=m_val+2:l_max;
y_jlm___(:,1+l_val,1+m_val) = ((2*l_val-1)*x_.*y_jlm___(:,1+l_val-1,1+m_val) - sqrt((l_val+m_val-1)*(l_val-m_val-1))*y_jlm___(:,1+l_val-2,1+m_val))/sqrt((l_val-m_val+0)*(l_val+m_val));
end;%for l_val=m_val+2:l_max;
end;%for m_val=0:l_max;
for l_val=0:l_max;
for m_val=0:l_val;
y_jlm___(:,1+l_val,1+m_val) = y_jlm___(:,1+l_val,1+m_val)*sqrt(2*l_val+1);
end;%for m_val=0:l_val;
end;%for l_val=0:l_max;
