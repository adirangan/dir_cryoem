function output = colormap_phase2(n_c,gamma_0,gamma_1,gamma_2);
if (nargin<1); n_c = 64; end;
if (nargin<2); gamma_0 = 0.50; end;
if (nargin<3); gamma_1 = 0.50; end;
if (nargin<4); gamma_2 = 0.50; end;

if n_c<0;
tmp__ = [[0:64-1],[0:64-1],[0:64-1]];
figure(1);clf;
p_row = 2; p_col = 4; np=0;
subplot_{1+np} = subplot(p_row,p_col,1+np);np=np+1;cla; imagesc(tmp__); axisnotick; 
subplot_{1+np} = subplot(p_row,p_col,1+np);np=np+1;cla; imagesc(tmp__); axisnotick; 
subplot_{1+np} = subplot(p_row,p_col,1+np);np=np+1;cla; imagesc(tmp__); axisnotick; 
subplot_{1+np} = subplot(p_row,p_col,1+np);np=np+1;cla; imagesc(tmp__); axisnotick; 
subplot_{1+np} = subplot(p_row,p_col,1+np);np=np+1;cla; imagesc(tmp__); axisnotick; 
subplot_{1+np} = subplot(p_row,p_col,1+np);np=np+1;cla; imagesc(tmp__); axisnotick; 
subplot_{1+np} = subplot(p_row,p_col,1+np);np=np+1;cla; imagesc(tmp__); axisnotick; 
subplot_{1+np} = subplot(p_row,p_col,1+np);np=np+1;cla; imagesc(tmp__); axisnotick; 
np=0;
colormap(subplot_{1+np},colormap_phase2(64,0.5,0.5,0.5)); title(subplot_{1+np},'0.5 0.5 0.5'); drawnow(); np=np+1;
colormap(subplot_{1+np},colormap_phase2(64,0.5,0.5,1.5)); title(subplot_{1+np},'0.5 0.5 1.5'); drawnow(); np=np+1;
colormap(subplot_{1+np},colormap_phase2(64,0.5,1.5,0.5)); title(subplot_{1+np},'0.5 1.5 0.5'); drawnow(); np=np+1;
colormap(subplot_{1+np},colormap_phase2(64,1.5,0.5,0.5)); title(subplot_{1+np},'1.5 0.5 0.5'); drawnow(); np=np+1;
colormap(subplot_{1+np},colormap_phase2(64,1.5,1.5,0.5)); title(subplot_{1+np},'1.5 1.5 0.5'); drawnow(); np=np+1;
colormap(subplot_{1+np},colormap_phase2(64,1.5,0.5,1.5)); title(subplot_{1+np},'1.5 0.5 1.5'); drawnow(); np=np+1;
colormap(subplot_{1+np},colormap_phase2(64,0.5,1.5,1.5)); title(subplot_{1+np},'0.5 1.5 1.5'); drawnow(); np=np+1;
colormap(subplot_{1+np},colormap_phase2(64,1.5,1.5,1.5)); title(subplot_{1+np},'1.5 1.5 1.5'); drawnow(); np=np+1;
disp('returning'); return;
end;%if n_c<0;

phi = 0*pi/2 ;
theta_ = 2*pi*transpose([0:n_c-1])/max(1,n_c);
phase_0_ = zeros(n_c,1);
tmp_index_0_ = efind(theta_>=2*pi*0/6 & theta_< 2*pi*1/6);
tmp_index_1_ = efind(theta_>=2*pi*1/6 & theta_< 2*pi*2/6);
tmp_index_2_ = efind(theta_>=2*pi*2/6 & theta_< 2*pi*3/6);
tmp_index_3_ = efind(theta_>=2*pi*3/6 & theta_< 2*pi*4/6);
tmp_index_4_ = efind(theta_>=2*pi*4/6 & theta_< 2*pi*5/6);
tmp_index_5_ = efind(theta_>=2*pi*5/6 & theta_< 2*pi*6/6);
phase_0_ = zeros(n_c,1);
phase_0_(1+tmp_index_0_) = 1.0;
phase_0_(1+tmp_index_1_) = 1.0 - (theta_(1+tmp_index_1_) - 2*pi*1/6)/(2*pi*2/6 - 2*pi*1/6) ;
phase_0_(1+tmp_index_2_) = 0.0;
phase_0_(1+tmp_index_3_) = 0.0;
phase_0_(1+tmp_index_4_) = 0.0 + (theta_(1+tmp_index_4_) - 2*pi*4/6)/(2*pi*5/6 - 2*pi*4/6) ;
phase_0_(1+tmp_index_5_) = 1.0;
phase_1_ = circshift(phase_0_,floor(1*n_c/3));
phase_2_ = circshift(phase_0_,floor(2*n_c/3));

c__ = zeros(n_c,3);
c__(:,1+0) = phase_0_.^gamma_0;
c__(:,1+1) = phase_1_.^gamma_1;
c__(:,1+2) = phase_2_.^gamma_2;
output = c__;
